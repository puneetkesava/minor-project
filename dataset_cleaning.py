from google.colab import drive
drive.mount('/content/drive')

import os
import shutil

# Paths
source_dir = '/content/drive/MyDrive/dataset'  # Update this to your Google Drive path
target_dir = '/content/drive/MyDrive/clean_dataset'

# Map your folder numbers to meaningful class names
label_map = {
    '1': 'selfie',
    '2': 'passport',
    '3': 'landscape',
    '4': 'object'
}

# Allowed image formats
allowed_exts = ['.jpg', '.jpeg', '.png', '.bmp']

# Process
for label_folder in os.listdir(source_dir):
    label_name = label_map.get(label_folder)  # Translate 1 -> selfie, etc.

    if label_name:
        current_folder = os.path.join(source_dir, label_folder)
        target_class_folder = os.path.join(target_dir, label_name)

        os.makedirs(target_class_folder, exist_ok=True)

        for filename in os.listdir(current_folder):
            ext = os.path.splitext(filename)[1].lower()
            if ext in allowed_exts:
                source_file = os.path.join(current_folder, filename)
                target_file = os.path.join(target_class_folder, filename)

                shutil.copy2(source_file, target_file)  # Copies file (keeps original)

print("✅ Dataset cleaned and sorted into:")
print(os.listdir(target_dir))